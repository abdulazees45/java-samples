package com.java.samples;

public class Arithmetic {
	float a1, b1;

	public float add(float a1, float b1) {
		this.a1 = a1;
		this.b1 = b1;
		return a1 + b1;
	}

	public float mul(float a1, float b1) {
		this.a1 = a1;
		this.b1 = b1;
		return a1 * b1;
	}

	public float sub(float a1, float b1) {
		this.a1 = a1;
		this.b1 = b1;
		return a1 - b1;

	}

	public float div(float a1, float b1) {
		this.a1 = a1;
		this.b1 = b1;
		return a1 / b1;
	}
}
