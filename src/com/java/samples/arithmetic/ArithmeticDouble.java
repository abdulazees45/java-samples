package com.java.samples.arithmetic;

public class ArithmeticDouble {
	double d1, d2;

	public double add(double d1, double d2) {
		this.d1 = d1;
		this.d2 = d2;
		return d1 + d2;
	}

	public double sub(double d1, double d2) {
		this.d1 = d1;
		this.d2 = d2;
		return d1 - d2;
	}

	public double mul(double d1, double d2) {
		this.d1 = d1;
		this.d2 = d2;
		return d1 * d2;
	}

	public double div(double d1, double d2) {
		this.d1 = d1;
		this.d2 = d2;
		return d1 / d2;
	}
}
